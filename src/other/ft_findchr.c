#include "../../include/vm.h"

int						ft_findchr(char *str, char c)
{
	int			id;

	id = 0;
	if (str)
	{
		while (str[id])
		{
			if (str[id] == c)
				return (id);
			id++;
		}
	}
	return (id);
}
